  <h2>Mission</h2>

  <p>The club aims to improve the ICT skills of its members by nurturing their creative and programming skills, and sharpening their research and presentation skills. The club also aims to promote innovation among its members as they seek out technological 
    solutions to problems that may arise in a school environment</p>
   
  <h2>Goals And Objectives</h2>

  <p>
    • To help other students become competent and confident users of ICT through training sessions. <br>
    • To develop policies concerning the use of technology at school.<br>
    • To help students develop an appreciation for the role of ICT in today's society.<br>
    • To help students develop as innovative beings who will see problems and create solutions.<br>
    • To provide a fun and interactive approach to ICT.<br>
    • To raise awareness and promote the use of technology as a teaching/learning aid.<br>
    • To provide ICT club students with sufficient skills to find solutions for in-house needs, such as timetable creation, duty allocation etc..<br>
    • To encourage students to develop as innovative beings, transforming them into confident and competent users.<br>
    • To encourage students should develop their own website and thereby turn into young entrepreneurs.<br>
    • To encourage students to propose their innovative ideas to the world through their participation in various coding events and bringing laurels to the institution.<br>

  </p>

